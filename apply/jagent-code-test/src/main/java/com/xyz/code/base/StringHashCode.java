package com.xyz.code.base;

public class StringHashCode {

    public static void main(String[] args) {
        String str1 = "Aa";
        System.out.println("Hash code of " + str1 + " is " + str1.hashCode());


        String str2 = "BB";
        System.out.println("Hash code of " + str2 + " is " + str2.hashCode());
    }
}
